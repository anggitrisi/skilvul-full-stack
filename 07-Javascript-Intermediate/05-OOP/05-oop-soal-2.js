// 1. Apa itu Encapsulation? Mengapa kita membutuhkannya?
/*
Encapsulation adalah sebuah konsep yang dimaksudkan untuk membungkus data variable dan fungsi menjadi satu kesatuan.Encapsulation juga adalah cara untuk membatasi akses langsung ke properti atau method dari sebuah objek.
Kita membutuhkan encapsulation untuk memastikan data tidak dapat diakses dari luar kelas sehingga tetap independen dan tetap  terjaga
*/

// 2. Apa itu Abstraction? Mengapa kita membutuhkannya?
/*
Abstraction adalah sebuah teknik untuk menyembunyikan detail proses yang tidak perlu diketahui pengguna, baik dari sebuah object atau method.
Kita membutuhkan abstraction untuk mengatur kompleksitas sistem dengan menyembunyikan detail dari internal sistem dan membuatnya ke dalam beberapa sistem kecil. Sehingga pengguna hanya perlu memberi suatu input untuk mendapatkan output yang diinginkan.
*/
// 3. Apa itu Inheritance? Mengapa kita membutuhkannya?
/*
Inheritance adalah dimana sebuah class mewariskan property dan methodnya ke class lain atau childnya.
Kita membutuhkan Inheritance untuk memnbuat kode kita menjadi lebih efisien karena kita menerapkan prinsi DRY(Dont Repeat Yourself)
*/
// 4. Apa itu Polymorpishm? Mengapa kita membutuhkannya?
/* 
Polymorpishm adalah konsep dalam OOP yang membuat suatu objek dapat memiliki banyak bentuk, dengan ini child class dapat memiliki method yang sama dengan parent tapi dapat memodifikasi isi methodnya.
Kita membutuhkan ini untuk melakukan overriding pada method yang ada child hasil dari turunan parent dengan memodifikasi isinya. Sehingga child dapat memiliki method yang sama dengan ini dengan hasil output yang berbeda menyesuaikan karakter dari child class tersebut. 
*/